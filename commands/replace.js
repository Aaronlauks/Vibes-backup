const ytdl = require('ytdl-core');
const queueVoice = require('../models/queueChannel.js');
exports.run = async (bot, message, args) => {
    let selectTime;
    let selectGame = "";
    if(!args[0]) return message.channel.send(`<:xcross:690880230562201610> please state the time you want to replace \`e.g. !replace 5am acnh, !replace 16 acnl etc.\``);
    if(!args[1]) return message.channel.send(`<:xcross:690880230562201610> please state the name of the animal crossing you want >:C`);
    let argsArgs = args[0].split("");
    if(args[1].toLowerCase() != "accf" && args[1].toLowerCase() != "acnh" && args[1].toLowerCase() != "acnl" && args[1].toLowerCase() != "cf" && args[1].toLowerCase() != "nh" && args[1].toLowerCase() != "nl" && args[1].toLowerCase() != "cityfolk" && args[1].toLowerCase() != "newhorizon" && !args[1].toLowerCase() != "newleaf") return message.channel.send(`<:xcross:690880230562201610> h-hey, that's not a valid game`);
            if(argsArgs.length > 2){
                if(argsArgs[argsArgs.length - 2].toUpperCase() + argsArgs[argsArgs.length - 1].toUpperCase() == "PM"){
                        argsArgs.splice(argsArgs.length - 1, 1);
                        argsArgs.splice(argsArgs.length - 1, 1);
                        let newArgs = argsArgs.join("");
                        if(newArgs < 1 || newArgs > 12) return message.channel.send(`<:xcross:690880230562201610> not a valid time lol`);
                        selectTime = +newArgs + 12;
                } else if(argsArgs[argsArgs.length - 2].toUpperCase() + argsArgs[argsArgs.length - 1].toUpperCase() == "AM"){
                    argsArgs.splice(argsArgs.length - 1, 1);
                    argsArgs.splice(argsArgs.length - 1, 1);
                    let newArgs = argsArgs.join("");
                    if(newArgs < 1 || newArgs > 12) return message.channel.send(`<:xcross:690880230562201610> not a valid time lol`);
                    selectTime = newArgs;
                } else return message.channel.send(`<:xcross:690880230562201610> not a valid time lol`);
            } else if(args[0] > 0 && args[0] < 25) {
                selectTime = args[0];
            } else return message.channel.send(`<:xcross:690880230562201610> not a valid time lol`);
            let queueChannel = await queueVoice.findOne({
                guildID: message.guild.id
              });
              let game = "";
              if(args[1].toLowerCase() == "accf" || args[1].toLowerCase() == "cf" || args[1].toLowerCase() == "cityfolk"){
                  queueChannel.queue[selectTime * 2 - 2] = ACCF[selectTime * 2 - 2];
                  queueChannel.queue[selectTime * 2 - 1] = ACCF[selectTime * 2 - 1];
                  game = "City Folk";
              } else if(args[1].toLowerCase() == "acnh" || args[1].toLowerCase() == "nh" || args[1].toLowerCase() == "newhorizon"){
                queueChannel.queue[selectTime * 2 - 2] = ACNH[selectTime * 2 - 2];
                queueChannel.queue[selectTime * 2 - 1] = ACNH[selectTime * 2 - 1];
                game = "New Horizon";
              } else {
                queueChannel.queue[selectTime * 2 - 2] = ACNL[selectTime * 2 - 2];
                queueChannel.queue[selectTime * 2 - 1] = ACNL[selectTime * 2 - 1];
                game = "New Leaf";
              }  
              let timeReplaced = "";
              if(selectTime < 13){
                  timeReplaced = `${selectTime}AM`
              } else {
                timeReplaced = `${selectTime - 12}PM`
              }
              message.channel.send(`<:tickGreen:690880245611626597> replaced ${timeReplaced} with Animal Crossing **${game}**!`)
              if(queueChannel.songNum == selectTime * 2 - 1 || queueChannel.songNum == selectTime * 2 - 2){
                music = queueChannel.queue[queueChannel.songNum];
                const channel = bot.channels.cache.get(queueChannel.voiceID);
                if(channel){
                channel.join().then(async connection => {
                  let dispatcher = await connection.play(ytdl(music));
              }).catch(e => console.error(e))
              }
            }
              await queueChannel.save().catch(e => console.log(e));
}
